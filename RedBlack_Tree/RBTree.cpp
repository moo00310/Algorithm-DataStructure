enum class ConsoleColor
{
	BLACK = 0,
	RED = FOREGROUND_RED,
	GREEN = FOREGROUND_GREEN,
	BLUE = FOREGROUND_BLUE,
	YELLOW = RED | GREEN,
	WHITE = RED | GREEN | BLUE

};

void SetCursorColor(ConsoleColor color)
{
	HANDLE output = ::GetStdHandle(STD_OUTPUT_HANDLE);
	::SetConsoleTextAttribute(output, static_cast<SHORT>(color));
}

void SetCursorPosition(int x, int y)
{
	HANDLE output = ::GetStdHandle(STD_OUTPUT_HANDLE);
	COORD pos = { static_cast<SHORT>(x), static_cast<SHORT>(y) };
	::SetConsoleCursorPosition(output, pos);
}

void ShowConsoleCursor(bool flag)
{
	HANDLE output = ::GetStdHandle(STD_OUTPUT_HANDLE);
	CONSOLE_CURSOR_INFO cursorInfo;
	::GetConsoleCursorInfo(output, &cursorInfo);
	cursorInfo.bVisible = flag;
	::SetConsoleCursorInfo(output, &cursorInfo);
}

RBTree::RBTree()
{ 
	//널 포인터 역할
	_nil = new Node(); //black
	_root = _nil;
}

RBTree::~RBTree()
{
	delete _nil;
}

void RBTree::Print()
{
	::system("cls");
	ShowConsoleCursor(false);
	{ Print(_root, 10, 0); }
}

void RBTree::Print(Node* node, int x, int y)
{
	if (node == _nil)
		return;

	SetCursorPosition(x, y);

	if (node->color == Color::Black)
		SetCursorColor(ConsoleColor::BLUE);
	else
		SetCursorColor(ConsoleColor::RED);

	cout << node->key;
	Print(node->left, x - (5 / (y + 1)), y + 1);
	Print(node->right, x + (5 / (y + 1)), y + 1);

	SetCursorColor(ConsoleColor::WHITE);
}

Node* RBTree::Search(Node* node, int key)
{
	if (node == _nil || key == node->key)
		return node;

	if (key < node->key)
		return Search(node->left, key);
	else
		return Search(node->right, key);
}

Node* RBTree::Min(Node* node)
{
	while (node->left != _nil)
	{
		node = node->left;
	}
	return node;
}

Node* RBTree::Max(Node* node)
{
	while (node->right != _nil)
	{
		node = node->right;
	}
	return node;
}

Node* RBTree::Next(Node* node)
{
	if (node->right != _nil)
		return Min(node->right);

	Node* parent = node->parent;

	while (parent != _nil && node == parent->right)
	{
		node = parent;
		parent = parent->parent;
	}

	return parent;
}

void RBTree::Insert(int key)
{
	Node* newNode = new Node();
	newNode->key = key;

	Node* node = _root;
	Node* parent = _nil;

	while (node != _nil)
	{
		parent = node;
		if (key < node->key)
			node = node->left;
		else
			node = node->right;
	}

	newNode->parent = parent;

	if (parent == _nil)
		_root = newNode;
	else if (key < parent->key)
		parent->left = newNode;
	else
		parent->right = newNode;

	//검사
	newNode->left = _nil;
	newNode->right = _nil;
	newNode->color = Color::Red;

	InsertFixup(newNode);
}

void RBTree::InsertFixup(Node* node)
{
	//1) p = red, uncle = red
	// -> p= black, uncle= black, pp = red로 바꿈
	// 
	//2) p = red, uncle = black (triangle)
	// -> 회전을 통해 case3으로 바꿔줌
	// 
	//3) p = red, uncle = black (list)
	// -> 색상병경 + 회전

	// [ppp](R)
	//       [pp(B)]            [pp(R)]       
	//  [p(R)]   [u(?)]  -> [p(B)]    [u(B)라면] 로 교체
	//     [n(R)]		       [n(R)]

	while (node->parent->color == Color::Red)
	{
		if (node->parent == node->parent->parent->left)
		{
			Node* uncle = node->parent->parent->right;
			if (uncle->color == Color::Red)
			{
				node->parent->color = Color::Black; //p
				uncle->color = Color::Black; //u
				node->parent->parent->color = Color::Red; //pp

				node = node->parent->parent; //[pp]의 조건도 검사
			}
			else
			{
				//     [pp(B)]       (Left Rotate)         [pp(B)]    
				//  [p(R)]   [u(B)]       ->		   [n(R)]    [u(B)] 
				//     [n(R)]					   [p(R)]		

				if (node == node->parent->right) //Triangle 타입
				{
					node = node->parent;
					LeftRotate(node);
				}


				//        [pp(R)]      (RightRotate)        [p(B)]    
				//   [p(B)]    [u(B)]        ->        [n(R)]    [pp(R)] 
				// [n(R)]									        [u(B)]

				//List 타입
				node->parent->color = Color::Black;
				node->parent->parent->color = Color::Red;
				RightRotate(node->parent->parent);
			}	
		}		
		else
		{
			//       [pp(B)]                  [pp(R)]       
			//  [u(R)]   [p(R)]       ->  [u(B)]    [p(B)]
			//              [n(R)]		                [n(R)]

			Node* uncle = node->parent->parent->left;
			if (uncle->color == Color::Red)
			{
				node->parent->color = Color::Black; //p
				uncle->color = Color::Black; //u
				node->parent->parent->color = Color::Red; //pp

				node = node->parent->parent; //[pp]의 조건도 검사
			}
			else
			{
				//      [pp(B)]         (Right Rotate)          [pp(B)]    
				//  [u(B)]     [p(R)]         ->		    [u(B)]    [p(R)] 
				//           [n(R)]					                     [n(R)]		

				if (node == node->parent->left) //Triangle 타입
				{
					node = node->parent;
					RightRotate(node);
				}

				//      [pp(B)]                                [pp(R)]         
				// [u(B)]    [p(R)]             ->        [u(B)]    [p(B)]     
				//               [n(R)]						              [n(R)]	

				//    (Left Rotate) 

				//          [p(B)]        
				//    [pp(R)]    [n(R)]    
				// [u(B)]

				//List 타입
				node->parent->color = Color::Black;
				node->parent->parent->color = Color::Red;
				LeftRotate(node->parent->parent);
			}
		}
	}

	_root->color = Color::Black;
}

void RBTree::Delete(int key)
{
	Node* deleteNode = Search(_root, key);
	Delete(deleteNode);
}

//먼저 BST 삭제 실행
void RBTree::Delete(Node* node)
{
	if (node == _nil)
		return;
	if (node->left == _nil)
	{
		Color color = node->color;
		Node* right = node->right;
		Replace(node, node->right);

		if (color == Color::Black)
			DeleteFixup(right);
	}
	else if (node->right == _nil)
	{
		Color color = node->color;
		Node* left = node->left;
		Replace(node, node->left);

		if (color == Color::Black)
			DeleteFixup(left);
	}
	else
	{
		Node* next = Next(node);
		node->key = next->key;
		Delete(next);
	}

}

//먼저 BST삭제 실행..
//- Case1) 삭제할 노드가 Red -> 그냥 삭제! 
// 
//- Case2) root가 DoubleBlack -> 그냥 Black 삭제!
// 
//- Case3) DoubleBlack의 sibling(형제)노드가 Red 이라면
//-- s = black, p= red (s <-> p 색상교환)
//-- DoubleBlack 방향으로 rotate(p)
//-- goto other case
//
//- Case4) DoubleBlack의 sibling 노드가 Black 이고 sibling 양쪽 자식도 black 이라면
//-- 추가 Black을 parent에게 이전
//-- p가 Red라면 Black이 됨
//-- p가 Black이면 DB가 됨
//-- s = red
//-- p를 대상으로 알고리즘 이어서 실행 (DB가 여전히 존재하면)
//
//- Case5) DB의 sibling 노드가 Black이고 sibling의 NearChild = red, ForChild = black 이면
//-- s <-> near 색상교환
//-- for 방향으로 rotate(s)
//-- goto Case6
//
//- Case6) DB의 sibling 노드가 Black이고 sibling의 ForChild = red 라면
//-- p <-> s 색상교환
//-- for = black
//-- rotate(p) (DB방향으로)
//-- 추가 Black 제거

void RBTree::DeleteFixup(Node* node)
{
	Node* x = node;
	//[Case1 / Case2]
	while (x != _root && x->color == Color::Black)
	{
		//     [p]
		//[x(DB)] [s(?)]
		if (x == x->parent ->left)
		{
			//[Case3]
			Node* s = x->parent->right;
			if (s->color == Color::Red)
			{
				s->color = Color::Black;
				x->parent->color = Color::Red;

				LeftRotate(x->parent);

				s = x->parent->right;
			}

			//[Case4]
			if (s->left->color == Color::Black && s->right->color == Color::Black)
			{
				s->color = Color::Red;
				x = x->parent;
			}
			else
			{

				//[Case5]
				if (s->right->color == Color::Black)
				{
					s->left->color = Color::Black;
					s->color = Color::Red;
					RightRotate(s);

					s = x->parent->right;
				}

				//[Case6]
				s->color = x->parent->color;
				x->parent->color = Color::Black;
				s->right->color = Color::Black;
				LeftRotate(x->parent);
				x = _root;
			}
		}
		else
		{
			//[Case3]
			Node* s = x->parent->left;
			if (s->color == Color::Red)
			{
				s->color = Color::Black;
				x->parent->color = Color::Red;

				RightRotate(x->parent);

				s = x->parent->left;
			}

			//[Case4]
			if (s->right->color == Color::Black && s->left->color == Color::Black)
			{
				s->color = Color::Red;
				x = x->parent;
			}
			else
			{
				//[Case5]
				if (s->left->color == Color::Black)
				{
					s->right->color = Color::Black;
					s->color = Color::Red;
					LeftRotate(s);

					s = x->parent->left;
				}

				//[Case6]
				s->color = x->parent->color;
				x->parent->color = Color::Black;
				s->left->color = Color::Black;
				RightRotate(x->parent);
				x = _root;
			}
		}
	}

	x->color = Color::Black;
}

//u를 v 서브트리로 교체
//그리고 delete u
void RBTree::Replace(Node* u, Node* v)
{
	if (u->parent == _nil)
		_root = v;
	else if (u == u->parent->left)
		u->parent->left = v;
	else
		u->parent->right = v;

		v->parent = u->parent;

	delete u;
}

//     [p]                                 [p]
//     [y]      (Right Rotate 시계방향)     [x]   
//  [x]   [3]             ->		    [1]     [y]  
//[1][2]	   	(Left Rotate 반시계방향)      [2] [3]
//                         <-

void RBTree::LeftRotate(Node* x)
{
	Node* y = x->right;

	x->right = y->left; //[2]

	if(y->left!=_nil)
		y->left->parent = x;

	y->parent = x->parent;

	if (x->parent == _nil)
		_root = y;
	else if (x == x->parent->left)
		x->parent->left = y;
	else
		x->parent->right = y;

	y->left = x;
	x->parent = y;

}

void RBTree::RightRotate(Node* y)
{
	Node* x = y->left;

	y->left = x->right;

	if (y->right != _nil)
		y->right->parent = y;

	x->parent = y->parent;

	if (y->parent == _nil)
		_root = x;
	else if (y == y->parent->right)
		y->parent->right = x;
	else
		y->parent->left = x;

	x->right = y;
	y->parent = x;
}
